def extract_brand(name: str) -> str:
    """从【】或标题前置提取品牌"""
    # 促销语黑名单
    promo_blacklist = ['专属', '限时', '福利', '特价', '直播', '周年', '新品', '热卖',
                       '视频', '到手', '拍一', '买二', '买三', '加赠', '试吃', '试喝',
                       '包邮', '顺丰', '冷链', '赠送', '赠品', '送', '多送']

    # 1. 【品牌名】模式
    m = re.search(r'【([^】]{2,10})】', name)
    if m:
        brand = m.group(1)
        if not any(kw in brand for kw in promo_blacklist):
            return brand

    # 2. [品牌名] 模式
    m = re.search(r'\[([^\]]{2,10})\]', name)
    if m:
        brand = m.group(1)
        if not any(kw in brand for kw in promo_blacklist):
            return brand

    # 3. 知名品牌词典匹配
    known_brands = [
        "蒙牛", "伊利", "光明", "三元", "君乐宝", "认养一头牛",
        "五粮液", "茅台", "泸州老窖", "剑南春", "汾酒", "洋河", "国台",
        "三只松鼠", "百草味", "良品铺子", "来伊份", "洽洽", "卫龙",
        "德叔鲍鱼", "锋味派", "海天", "李锦记", "老干妈",
        "东鹏特饮", "农夫山泉", "怡宝", "百事", "可口可乐",
        "白象", "今麦郎", "康师傅", "统一",
        "正大", "圣农", "大希地", "温氏",
        "诺特兰德", "汤臣倍健", "Swisse",
        "麦富迪", "皇家", "渴望", "爱肯拿",
        "福月德", "如意三宝", "天海藏", "口水娃",
        "桂格", "家乐", "美极",
    ]
    for brand in sorted(known_brands, key=len, reverse=True):
        if brand in name:
            return brand

    # 4. 品牌前置（前3-5字+空格/分隔符）
    m = re.match(r'^([^\s\[\(【\(]{2,8})[\s\[\(【]', name)
    if m:
        brand = m.group(1)
        if brand.isdigit() or brand in ['新', '纯', '正宗', '精品', '优质']:
            return ""
        if not any(kw in brand for kw in promo_blacklist):
            return brand

    return ""


def extract_product(name: str, cat: str) -> str:
    """从品类关键词映射提取产品名"""
    subcats = SUBCAT_MAP.get(cat, {})
