def extract_spec(name: str) -> str:
    """提取规格"""
    patterns = [
        r'(\d+\.?\d*\s*[kK][gG])',
        r'(\d+\s*斤)',
        r'(\d+\s*[mM][lL])',
        r'(\d+\s*[lL])',
        r'(\d+\.?\d*\s*g(?!m))',
        r'(\d+只)',
        r'(\d+枚)',
        r'(\d+支)',
        r'(\d+瓶)',
        r'(\d+罐)',
        r'(\d+盒)',
        r'(\d+袋)',
        r'(\d+包)',
        r'(\d+条)',
    ]
    for p in patterns:
        m = re.search(p, name)
        if m:
            return m.group(1)
    return ""


